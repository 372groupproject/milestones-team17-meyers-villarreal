object p3_boolean {
  def boolean_ops(): Unit =  {
    println(true != false)
    println(true == false)
    println(true & false)
    println(true && false)
    println(true ^ false)
    println(!false)
    println(true | false)
    println(true || false)
    println(1 < 2)
    println(1 <= 2)
    println(1 > 2)
    println(1 >= 2)
    println(true.compareTo(false))
  }
}
